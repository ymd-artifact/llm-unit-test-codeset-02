public class Class1E0001 {
    // Class (static) field pattern
    private static int abc = 100;

    public static int func1E0001() {
        return abc * 3;
    }

    // Getter and setter for class field
    public static int getAbc() {
        return abc;
    }

    public static void setAbc(int value) {
        abc = value;
    }
}